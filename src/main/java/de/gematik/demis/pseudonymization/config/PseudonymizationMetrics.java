import io.micrometer.core.instrument.Counter;
import io.micrometer.core.instrument.MeterRegistry;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Service;

@Service
public class PseudonymizationMetrics {

  @Autowired private final MeterRegistry registry;

  private final Counter totalCounter;

  public PseudonymizationMetrics(final MeterRegistry registry) {
    this.registry = registry;
    this.totalCounter = this.registry.counter("pseudonymization_total_generations");
  }

  public void incrementTotal() {
    this.totalCounter.increment();
  }
}
